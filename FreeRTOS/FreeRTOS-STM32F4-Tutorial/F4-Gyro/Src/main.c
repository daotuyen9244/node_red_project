/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_I2S3_Init();
  MX_SPI1_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */
  /* Initialize the L3GD20 Gyroscope */
  if (BSP_ACCELERO_Init() != HAL_OK)
  {
    Error_Handler();
  }
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    /* Ready gyro angular data */
    BSP_ACCELERO_GetXYZ(buffer);
    HAL_Delay(250);
    Send_Data_to_USB(buffer);
    xval = buffer[0];
    yval = buffer[1];
    /* Manage data */

    if ((ABS(xval)) > (ABS(yval)))
    {
      if (xval > ThresholdHigh)
      {
        /* LED5 On */
        BSP_LED_On(LED5);
        HAL_Delay(250);
      }
      else if (xval < ThresholdLow)
      {
        /* LED4 On */
        BSP_LED_On(LED4);
        HAL_Delay(250);
      }
      else
      {
        HAL_Delay(250);
      }
    }
    else
    {
      if (yval < ThresholdLow)
      {
        /* LED6 On */
        BSP_LED_On(LED6);
        HAL_Delay(250);
      }
      else if (yval > ThresholdHigh)
      {
        /* LED3 On */
        BSP_LED_On(LED3);
        HAL_Delay(250);
      }
      else
      {
        HAL_Delay(250);
      }
    }

    BSP_LED_Off(LED3);
    BSP_LED_Off(LED4);
    BSP_LED_Off(LED5);
    BSP_LED_Off(LED6);
  }
  /* USER CODE END 3 */
}

